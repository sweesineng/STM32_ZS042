/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */
  printf("\r\nStart Demo ZS042 Module\n");
  uint8_t Year = 21;
  uint8_t Month = 7;
  uint8_t Day = 6;
  uint8_t DayOfWeek = 2;
  uint8_t Hour = 15;
  uint8_t Minute = 0;
  uint8_t Second = 0;

  /* Disable Battery Backup Square Wave*/
  DS3231_BaterryBackedSQW(DS3231_Disable);

  /* Disable Oscillator */
  DS3231_OscillatorEOSC(DS3231_Disable);

  /* Disable 32kHz Output */
  DS3231_32kHzOutput(DS3231_Disable);

  /* Enable Temperature Conversion */
  DS3231_EnableConvTemp(DS3231_Enabled);

  /* Set RTC time */
  DS3231_TimeSet(Second, Minute, Hour, DayOfWeek, Day, Month, Year);

  /* Set Alarm 1 */
  uint8_t A1_Date 	= 0;	// Date of month
  uint8_t A1_Hour 	= 15;
  uint8_t A1_Minute = 1;
  uint8_t A1_Sec 	= 30;
  DS3231_SetAlarm1(MODE_HOUR_MIN_SEC, A1_Sec, A1_Minute, A1_Hour, A1_Date);

  /* Set Alarm 2 */
  uint8_t A2_Day 	= 6;	// Day of week(refer to conf on header file)
  uint8_t A2_Hour 	= 15;
  uint8_t A2_Minute = 1;
  DS3231_SetAlarm2(MODE_ALL, A2_Minute, A2_Hour, A2_Day);

  /* Write Alarm 1 into EEPROM */
  AT24Cxx_EraseChip();
  uint8_t ATBuff[] = {A1_Sec, A1_Minute, A1_Hour, A1_Date};
  AT24Cxx_WriteByte(0, ATBuff, sizeof(ATBuff));
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  DS3231_TimeGet(&Now);
	  printf("%d-%d-%d %d:%d:%d %0.2fC\n", Now.dayofmonth, Now.month,
			  Now.year, Now.hour, Now.minutes, Now.seconds, DS3231_GetTemp());
	  if (DS3231_GetFlag(DS3231_STAT_A1F) == 1)
	  {
		  /* Read back the time data from AT24Cxx */
		  uint8_t AL1[4];
		  AT24Cxx_ReadByte(0, AL1, sizeof(AL1));
		  printf("Alarm1 trigger at %d:%d:%d\n", AL1[2], AL1[1], AL1[0]);
		  DS3231_ClearAlarm(A1);
	  }
	  if (DS3231_GetFlag(DS3231_STAT_A2F) == 1)
	  {
		  printf("Alarm 2 trigger\n");
		  DS3231_ClearAlarm(A2);
	  }
	  HAL_Delay(1000);
  }
  /* USER CODE END 3 */
}

